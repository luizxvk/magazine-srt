export async function reviewCouponApproval(req: Request, res: Response) {
  try {
    const { id } = req.params;
    const { approved, reviewNote } = req.body;

    const coupon = await prisma.coupon.update({
      where: { id },
      data: {
        rovexApproval: approved ? 'APPROVED' : 'REJECTED',
        rovexReviewedAt: new Date(),
        rovexReviewNote: reviewNote || null,
      },
    });

    res.json(coupon);
  } catch (error) {
    console.error('[Coupons] Error reviewing approval:', error);
    res.status(500).json({ error: 'Failed to review coupon approval' });
  }
}

// ============================================
// GENERATE ELITE MONTHLY COUPON
// ============================================
export async function generateEliteCoupon(req: Request, res: Response) {
  try {
    const userId = (req as any).user.id;

    // Check if user is Elite
    const user = await prisma.user.findUnique({
      where: { id: userId },
      select: { isElite: true, name: true },
    });

    if (!user?.isElite) {
      return res.status(403).json({ error: 'Only Elite members can claim monthly coupons' });
    }

    // Check if already generated this month
    const now = new Date();
    const monthStart = new Date(now.getFullYear(), now.getMonth(), 1);
    const monthEnd = new Date(now.getFullYear(), now.getMonth() + 1, 0, 23, 59, 59);

    const existingCoupon = await prisma.coupon.findFirst({
      where: {
        createdById: userId,
        isAutoGenerated: true,
        createdAt: { gte: monthStart, lte: monthEnd },
      },
    });

    if (existingCoupon) {
      return res.json({ coupon: existingCoupon, alreadyClaimed: true });
    }

    // Generate unique code
    const code = `ELITE-${userId.slice(0, 4).toUpperCase()}-${now.getMonth() + 1}${now.getFullYear().toString().slice(-2)}`;

    const coupon = await prisma.coupon.create({
      data: {
        code,
        description: `Cupom Elite mensal de ${user.name}`,
        discountType: 'FIXED',
        discountValue: 10, // R$10 fixo
        maxUses: 1,
        maxUsesPerUser: 1,
        isEliteOnly: false, // Anyone can use if they know the code
        isAutoGenerated: true,
        expiresAt: monthEnd,
        rovexApproval: 'APPROVED', // Auto-approved for Elite
        createdById: userId,
      },
    });

    res.json({ coupon, alreadyClaimed: false });
  } catch (error) {
    console.error('[Coupons] Error generating elite coupon:', error);
    res.status(500).json({ error: 'Failed to generate elite coupon' });
  }
}
